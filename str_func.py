def get_upper_case(word: str):
    """
    makes a string in capital letters
    :param word: str
    :return: str
    """
    return word.upper()


def get_title(word: str):
    """
    Accepts a string and return a string
    in which all the first letters of the words are uppercase
    :param word: str
    :return: str
    """
    return word.title()
